    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            byte[] sourcePath = lblSourcePath.getText().getBytes();
            byte[] desPath = lblDesPath.getText().getBytes();
            byte[] bai = String.valueOf("1").getBytes();
            
            DatagramPacket bai1 = new DatagramPacket(bai, bai.length, host, port);
            client.send(bai1);
            DatagramPacket sourceAddress = new DatagramPacket(sourcePath, sourcePath.length, host, port);
            client.send(sourceAddress);
            DatagramPacket desAddress    = new DatagramPacket(desPath   , desPath.length   , host, port);
            client.send(desAddress);
            
            byte[] message = new byte[1024];
            DatagramPacket kq = new DatagramPacket(message, message.length);
            client.receive(kq);
            txtAreaBai1.append(new String(message,0, message.length));
        } catch (IOException ex) {
            txtAreaBai1.setText("Lỗi truyền tin!");
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

