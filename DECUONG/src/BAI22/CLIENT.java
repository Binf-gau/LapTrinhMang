    private void bttSourceChooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttSourceChooseActionPerformed
        JFileChooser sourceChooser = new JFileChooser();
        sourceChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        sourceChooser.setAcceptAllFileFilterUsed(false);
        int x = sourceChooser.showDialog(this, "Select");
        if (x == sourceChooser.APPROVE_OPTION) {
            File sourceFile = sourceChooser.getSelectedFile();
            lblSourcePath.setText(sourceFile.getPath());
        }
    }//GEN-LAST:event_bttSourceChooseActionPerformed

